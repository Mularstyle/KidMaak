/**
 * Find matching mock scenario based on situation keywords
 */
export function findMockScenario(situation: string): MockScenario | null {
  const lowerSituation = situation.toLowerCase();
  for (const scenario of MOCK_SCENARIOS) {
    if (scenario.keywords.some((kw) => lowerSituation.includes(kw))) {
      return scenario;
    }
  }
  return null;
}

/**
 * Get mock thought chain response for a given situation and level
 */
export function getMockGenerateResponse(
  situation: string,
  level: OverthinkLevel,
): GenerateResponse {
  const scenario = findMockScenario(situation);
  const steps = scenario ? scenario.steps[level] : FALLBACK_STEPS[level];

  return {
    steps,
    sanity_score: getRandomSanityScore(level),
    ending_type: "normal",
  };
}

/**
 * Get mock alternate ending response
 */
export function getMockAlternateEndingResponse(
  level: OverthinkLevel,
  endingType: EndingType,
): AlternateEndingResponse {
  return {
    new_final_step: ALTERNATE_ENDINGS[endingType][level],
    sanity_score: getRandomSanityScore(level),
    ending_type: endingType,
  };
}
