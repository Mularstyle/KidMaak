// Sanity score ranges per level
const SANITY_RANGES: Record<OverthinkLevel, [number, number]> = {
  1: [80, 100],
  2: [60, 80],
  3: [40, 60],
  4: [20, 40],
  5: [0, 20],
};

export function getRandomSanityScore(level: OverthinkLevel): number {
  const [min, max] = SANITY_RANGES[level];
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

